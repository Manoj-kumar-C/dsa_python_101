# Hashing a string
#sha256 algo
my_string = "Hello, World!"
hashed_string = hash(my_string)
print("Hashed string:", hashed_string)

# Hashing an integer
my_integer = 1.5
hashed_integer = hash(my_integer)
print("Hashed integer:", hashed_integer)

# Hashing a tuple
my_tuple = (1, 2, 3) # 
hashed_tuple = hash(my_tuple)
print("Hashed tuple:", hashed_tuple)
